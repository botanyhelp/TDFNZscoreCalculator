        //startActivityForResult(iZscore,0);
        String ageChoice = TDFNZscoreCalculator.ageChoice;
        //TextView tv = (TextView) findViewById(R.id.txt_Age);
        String sexChoice = TDFNZscoreCalculator.sexChoice;
        TextView tv = (TextView) findViewById(R.id.txt_MeasurementParam);
        String measurementChoice = tv.getText().toString();
        TDFNZscoreCalculator.measurementChoice = measurementChoice;
        tv = (TextView) findViewById(R.id.txt_Measurement);
        tv.setText(measurementChoice);
        //TextView tv = (TextView) findViewById(R.id.txt_Age);
        tv = (TextView) findViewById(R.id.txt_Age);
